public class printAndprintln{
  public static void main(String[] args){
 
  /** This example is using print
    * output: HelloWorld!
    */
    System.out.print("Hello");
    System.out.print("World");
    System.out.print("!");
    
    System.out.println(); // printing to next line.
    
  /** This example is using println
     * output:
     * Hello
     * World
     * !
     */    
    System.out.println("Hello");
    System.out.println("World");
    System.out.println("!");
    
    
  /** This example is useing both: print and println
    * output: HelloWorld!
    */
    System.out.print("Hello");
    System.out.print("World");
    System.out.println("!");
  }
}